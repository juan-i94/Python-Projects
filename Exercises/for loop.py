# Syntax of for loop

# For i in range/sequence:
#     statement 1
#     statement 2
#     statement n
# givenlist = [10, 20, 33, 46, 55]
#
# for n in givenlist:
#     if n % 5 == 0:
#         print(n)
# In the syntax, i is the iterating variable, and the range specifies how many times the loop should run.
# For example, if a list contains 10 numbers then 'for loop' will execute 10 times to print each number.
# In each iteration of the loop, the variable i get the current value.

for i in range(6):
    print(i)

